                int xpoints[] = new int[point.size()];
                int ypoints[] = new int[point.size()];
                for(int i = 0; i < point.size(); i++){
                    xpoints[i] = (int)point.get(i).getX();
                    ypoints[i] = (int)point.get(i).getY();
                }
                Polygon poly = new Polygon(xpoints, ypoints,point.size());
